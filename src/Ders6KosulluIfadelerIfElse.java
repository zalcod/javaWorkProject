public class Ders6KosulluIfadelerIfElse {
    public static void main(String[] args) {
        /// 6. Ders Koşullu İfadeler if,else...
        System.out.println("6. Ders Koşullu İfadeler if,else...");
        int a = 5 , b = 10;
        if (a>b) {
            System.out.println("a, b'den büyüktür.");
        } else if (a<b) {
            System.out.println("a, b'den küçüktür.");
        } else {
            System.out.println("a, b'ye eşittir.");
        }
    }
}
